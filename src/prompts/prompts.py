# ============================================================
# Pydantic Schema（结构化输出）
# ============================================================
class SurveyOutput(BaseModel):
    """综述结构化输出"""
    overview: str = Field(description="研究领域总体概述")
    papers: List[dict] = Field(description="论文分析列表 [{title, method, contribution, highlights}]")
    comparison: dict = Field(description="方法对比")
    trends: List[str] = Field(description="研究热点和趋势（3-5 条）")
    conclusion: str = Field(description="结论与展望")
    references: List[str] = Field(description="引用的论文列表")


class CompareOutput(BaseModel):
    """对比分析结构化输出"""
    overview: str = Field(description="总体对比")
    dimensions: List[dict] = Field(description="各维度对比")
    winner: str = Field(description="综合最优推荐")
    recommendation: str = Field(description="推荐理由")
